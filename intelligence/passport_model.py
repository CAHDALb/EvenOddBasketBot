"""
passport_model.py

Единая модель Signal Passport.

Все аналитические модули проекта должны работать
с одной и той же структурой паспорта.

Если в будущем нам потребуется добавить новые поля,
структуру меняем централизованно именно в этом файле.
"""

from datetime import datetime


# ------------------------------------------------------------
# Текущая версия структуры Signal Passport
# ------------------------------------------------------------

PASSPORT_VERSION = "1.0"

# Название программы, создавшей паспорт
PASSPORT_GENERATOR = "EvenOddBasketBot"


def create_passport() -> dict:
    """
    Создаёт новый пустой Signal Passport.

    Функция возвращает словарь с заранее определённой
    структурой. Остальные модули проекта не должны
    создавать паспорт вручную.

    Возвращает:
        dict: новый пустой Signal Passport.
    """

    passport = {

        # ----------------------------------------------------
        # Версия структуры паспорта
        # ----------------------------------------------------

        "version": PASSPORT_VERSION,

        # ----------------------------------------------------
        # Историческая статистика
        # ----------------------------------------------------

        # Статистика по стране
        "country": {},

        # Статистика по конкретной лиге
        "league": {},

        # Статистика по типу матча:
        # мужчины, женщины, молодёжные и т. д.
        "match_type": {},

        # ----------------------------------------------------
        # Итоговые аналитические показатели
        # ----------------------------------------------------

        # Историческая оценка качества сигнала от 0 до 100
        "signal_score": None,

        # Уровень доверия к оценке от 0 до 100%
        "confidence": None,

        # Уровень надёжности:
        # insufficient, bronze, silver или gold
        "level": None,

        # Итоговая рекомендация аналитической системы
        "recommendation": None,

        # ----------------------------------------------------
        # Дополнительная история
        # ----------------------------------------------------

        # В дальнейшем здесь могут появиться:
        # последние сигналы, серии побед и поражений,
        # динамика ROI и другая история.
        "history": {},

        # ----------------------------------------------------
        # Служебная информация
        # ----------------------------------------------------

        "meta": {

            # Дата и время создания паспорта
            "created_at": datetime.now(),

            # Название программы, создавшей паспорт
            "generator": PASSPORT_GENERATOR,
        },
    }

    return passport